/*
 * This class develops the nodes that will comprise the 
 * doubly, multi linked lists that form the matrices
 * 
 * It takes no inputs, and holds pointers to prev and next
 * as well as up and down
 */
public class LinkedNode {
        int data; 
        LinkedNode next = null;  
        LinkedNode prev = null;  
        LinkedNode up = null;  
        LinkedNode down = null;
}
